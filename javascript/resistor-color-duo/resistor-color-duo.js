export const decodedValue = (colors) => {
  return Number(colors.map(colorCode).splice(0,2).join(''));
};

const colorCode = (color) => {
  return COLORS.indexOf(color);
};

const COLORS = [
  "black",
  "brown",
  "red",
  "orange",
  "yellow",
  "green",
  "blue",
  "violet",
  "grey",
  "white"
];
